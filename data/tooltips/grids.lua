function getTooltipGridCHN(desc)
	if not desc then return end

	for i = 1,#desc do
		if type(desc[i]) == "string" then 
			desc[i] = gridCHN[desc[i]] or desc[i] 
			if string.find(desc[i],"range:") then desc[i] = desc[i]:gsub("range","距离") end
		end

	end

	return desc
end
